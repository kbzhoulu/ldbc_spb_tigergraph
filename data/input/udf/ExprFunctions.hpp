#include <iostream>
#include <algorithm>
#include <string>


inline float str_to_float(std::string str) {

    // convert string to float
    float num_float = std::stof(str);

    return num_float;
}


inline double str_to_double(std::string str) {

    // convert string to double
    double num_double = std::stod(str);

    return num_double;
}




 



























